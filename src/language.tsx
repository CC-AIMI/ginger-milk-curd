function toTraditional(value: string) {
  let result = value;
  Object.entries(phraseMap)
    .sort((a, b) => b[0].length - a[0].length)
    .forEach(([simplified, traditional]) => {
      result = result.split(simplified).join(traditional);
    });

  return result.replace(/[\u4e00-\u9fff]/g, (char) => charMap[char] ?? char);
}

const LanguageContext = createContext<LanguageContextValue | null>(null);

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguage] = useState<Language>('zh-CN');

  const value = useMemo<LanguageContextValue>(() => ({
    language,
    setLanguage,
    text: (source: string) => (language === 'zh-HK' ? toTraditional(source) : source),
  }), [language]);

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useLanguage() {
  const value = useContext(LanguageContext);
  if (!value) {
    throw new Error('useLanguage must be used within LanguageProvider');
  }
  return value;
}
